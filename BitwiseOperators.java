public class BitwiseOperators{
    public static void main(String[] args) {
        System.out.println(5 & 6); //AND Bit-wise operator
        System.out.println(5 | 6); //OR Bit-wise operator
        System.out.println(5 ^ 6); //XOR Bit-wise operator
        System.out.println(~5); //One's Complement Bit-wise operator
        System.out.println(5 << 2); //Binary left shift operator
        System.out.println(6 >> 1); //Binary right shift operator
    }
}